# 小时模型
class Dataset_ETT_hour(Dataset):
    def __init__(self, subject, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, inverse=False, timeenc=0, freq='h', cols=None):
        self.seq_len = size[0]
        self.label_len = size[1]
        self.pred_len = size[2]
        # init
        assert flag in ['train', 'val', 'test']
        type_map = {'train': 'T', 'val': 'E', 'test': 'E'}

        self.set_type = type_map[flag]
        self.subject = subject
        self.features = features
        self.target = target
        self.scale = scale
        self.inverse = inverse
        self.timeenc = timeenc
        self.freq = freq
        self.index_len = 0
        self.Get_Frequence = 250
        self.Retain_time = 3
        self.Rejust_List = []
        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        Data_Load_Path = self.root_path + '\\' + self.subject + self.set_type + '.pt'
        print("载入路径", Data_Load_Path)
        Data_Tensor = torch.load(Data_Load_Path)
        print("数据尺寸", Data_Tensor.shape)
        channel_num, Data_Get_Len = Data_Tensor.shape
        self.index_len = Data_Get_Len - self.seq_len - self.pred_len
        get_period = 15
        Now_Data = random.sample(range(1, get_period), 1)[0]
        self.Rejust_List.append(Now_Data)

        # 随机产生样本
        while Now_Data < self.index_len - get_period:
            index_add = random.sample(range(1, get_period), 1)[0]
            Now_Data = Now_Data + index_add
            self.Rejust_List.append(Now_Data)

        self.data_x = torch.clone(Data_Tensor.t())
        self.data_y = torch.clone(Data_Tensor.t())

        time_Len = self.seq_len + self.pred_len
        time_index = torch.linspace(1, time_Len, time_Len)
        self.data_stamp = time_index / time_Len - 0.5

    def __getitem__(self, index):
        Lable_be = 4
        s_begin = self.Rejust_List[index]
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len - Lable_be
        r_end = s_end - Lable_be

        seq_x = self.data_x[s_begin:s_end, :4]
        seq_y = self.data_y[r_begin:r_end, :]
        seq_x_mark = torch.unsqueeze(self.data_stamp[0:self.seq_len], dim=1)
        seq_y_mark = torch.unsqueeze(self.data_stamp[self.seq_len - self.label_len:self.seq_len], dim=1)
        return seq_x, seq_y, seq_x_mark, seq_y_mark
    
    def __len__(self):
        INDEX_num = len(self.Rejust_List)
        # INDEX_num = min(10000, INDEX_num)
        return INDEX_num

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


class Dataset_ETT_minute(Dataset):
    def __init__(self, root_path, flag='train', size=None, 
                 features='S', data_path='ETTm1.csv', 
                 target='OT', scale=True, inverse=False, timeenc=0, freq='t', cols=None):
        if size == None:
            self.seq_len = 24*4*4
            self.label_len = 24*4
            self.pred_len = 24*4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train':0, 'val':1, 'test':2}
        self.set_type = type_map[flag]
        
        self.features = features
        self.target = target
        self.scale = scale
        self.inverse = inverse
        self.timeenc = timeenc
        self.freq = freq
        
        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12*30*24*4 - self.seq_len, 12*30*24*4+4*30*24*4 - self.seq_len]
        border2s = [12*30*24*4, 12*30*24*4+4*30*24*4, 12*30*24*4+8*30*24*4]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]
        
        if self.features == 'M' or self.features=='MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features=='S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values
            
        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        data_stamp = time_features(df_stamp, timeenc=self.timeenc, freq=self.freq)
        
        self.data_x = data[border1:border2]
        if self.inverse:
            self.data_y = df_data.values[border1:border2]
        else:
            self.data_y = data[border1:border2]
        self.data_stamp = data_stamp
    
    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        if self.inverse:
            seq_y = np.concatenate([self.data_x[r_begin:r_begin+self.label_len], self.data_y[r_begin+self.label_len:r_end]], 0)
        else:
            seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]


        return seq_x, seq_y, seq_x_mark, seq_y_mark
    
    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


